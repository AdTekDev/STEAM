
// Vòng lặp For hoặc While in ra tuần tự các số

let N = 5;
let i = 1;

// FOR 
for(i=1; i<=N; i++) {
    console.log("For ", i);
}

console.log("hết For bắt đầu while thì i= ", i);

// WHILE
i = 1;
while(i<=N) {
    console.log("While ", i);
    i++;
}

console.log("hết while thì i= ", i);